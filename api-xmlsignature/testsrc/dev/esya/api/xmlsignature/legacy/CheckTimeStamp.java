    public boolean checkTimeStampValue(String baseDir, String fileName) throws ESYAException, NoSuchAlgorithmException, IOException, InvalidKeyException, SignatureException, XMLSignatureException {
        XMLSignature signature = XMLSignature.parse(
                new FileDocument(new File(baseDir+fileName)),
                new Context(baseDir)) ;

        SignatureTimeStamp timeStamp = signature.getQualifyingProperties().getUnsignedProperties().getUnsignedSignatureProperties().getSignatureTimeStamp(0);

        int timeStampCount = timeStamp.getEncapsulatedTimeStampCount();
        boolean testResult=false;
        System.out.println(fileName+" "+timeStampCount);
        for(int i=0;i<timeStampCount;i++)
        {
            EncapsulatedTimeStamp encTimeStamp = timeStamp.getEncapsulatedTimeStamp(i);
            DigestAlg alg = encTimeStamp.getDigestAlgorithm();
            byte [] digestValue = encTimeStamp.getDigestValue();
            Calendar cal = encTimeStamp.getTime();
            testResult = encTimeStamp.isTimeStamp();
        }

        return testResult;
    }

